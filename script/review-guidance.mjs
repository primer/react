import {createHash} from 'node:crypto'
import {existsSync, mkdirSync, readFileSync, readdirSync, rmSync, writeFileSync} from 'node:fs'
import {dirname, join, relative, resolve} from 'node:path'
import {fileURLToPath, pathToFileURL} from 'node:url'

const scriptDirectory = dirname(fileURLToPath(import.meta.url))
const repositoryRoot = resolve(scriptDirectory, '..')
const publicPolicyPath = join(repositoryRoot, 'contributor-docs/review-guidance/policy.json')
const internalPolicyPath = join(repositoryRoot, 'contributor-docs/review-guidance/internal-policy.json')
const instructionsDirectory = join(repositoryRoot, '.github/instructions/generated')
const skillReferencePath = join(repositoryRoot, '.github/skills/primer-component-review/references/generated-policy.md')
const generatedMarker = '<!-- Generated by script/review-guidance.mjs. Do not edit directly. -->'

function readJson(path) {
  return JSON.parse(readFileSync(path, 'utf8'))
}

export function sourceDigest(sourcePaths) {
  const hash = createHash('sha256')
  for (const sourcePath of sourcePaths) {
    hash.update(sourcePath)
    hash.update(readFileSync(join(repositoryRoot, sourcePath)))
  }
  return hash.digest('hex').slice(0, 12)
}

function assertAllowedKeys(value, allowedKeys, label) {
  for (const key of Object.keys(value)) {
    assert(allowedKeys.includes(key), `${label} contains unsupported field: ${key}`)
  }
}

function assert(condition, message) {
  if (!condition) {
    throw new Error(message)
  }
}

export function validatePolicies(publicPolicy, internalPolicy) {
  assertAllowedKeys(publicPolicy, ['version', 'ruleSets'], 'Public policy')
  assertAllowedKeys(internalPolicy, ['version', 'ruleSets'], 'Internal policy')
  assert(publicPolicy.version === 1, 'Public policy must use version 1')
  assert(internalPolicy.version === 1, 'Internal policy must use version 1')
  assert(Array.isArray(publicPolicy.ruleSets), 'Public policy must include ruleSets')
  assert(Array.isArray(internalPolicy.ruleSets), 'Internal policy must include ruleSets')

  const ruleSetIds = new Set()
  const ruleIds = new Set()
  const supersededRuleIds = new Set()
  const privateText = JSON.stringify(internalPolicy)
  const forbiddenPrivatePatterns = [
    /https?:\/\//i,
    /github\/primer/i,
    /slack/i,
    /figma/i,
    /discussion/i,
    /issue[#\s-]*\d+/i,
    /rationale/i,
    /alternative/i,
    /consequence/i,
  ]

  for (const pattern of forbiddenPrivatePatterns) {
    assert(!pattern.test(privateText), `Internal policy contains forbidden private-source text: ${pattern}`)
  }

  for (const [policyName, policy] of [
    ['public', publicPolicy],
    ['internal', internalPolicy],
  ]) {
    for (const ruleSet of policy.ruleSets) {
      assertAllowedKeys(
        ruleSet,
        policyName === 'public'
          ? ['id', 'title', 'applyTo', 'sources', 'sourceDigest', 'rules']
          : ['id', 'title', 'applyTo', 'rules'],
        `${policyName} rule set`,
      )
      assert(typeof ruleSet.id === 'string' && ruleSet.id.length > 0, `${policyName} rule set needs an id`)
      assert(!ruleSetIds.has(ruleSet.id), `Duplicate rule set id: ${ruleSet.id}`)
      ruleSetIds.add(ruleSet.id)
      assert(typeof ruleSet.title === 'string' && ruleSet.title.length > 0, `${ruleSet.id} needs a title`)
      assert(typeof ruleSet.applyTo === 'string' && ruleSet.applyTo.length > 0, `${ruleSet.id} needs applyTo`)
      assert(Array.isArray(ruleSet.rules) && ruleSet.rules.length > 0, `${ruleSet.id} needs rules`)

      if (policyName === 'public') {
        assert(Array.isArray(ruleSet.sources) && ruleSet.sources.length > 0, `${ruleSet.id} needs sources`)
        for (const source of ruleSet.sources) {
          assert(existsSync(join(repositoryRoot, source)), `Missing source for ${ruleSet.id}: ${source}`)
        }
        assert(
          ruleSet.sourceDigest === sourceDigest(ruleSet.sources),
          `Sources changed for ${ruleSet.id}. Review its rules, then run npm run accept:review-guidance-sources.`,
        )
      } else {
        assert(ruleSet.sources === undefined, `Internal rule set ${ruleSet.id} must not expose sources`)
      }

      for (const rule of ruleSet.rules) {
        assertAllowedKeys(
          rule,
          policyName === 'public'
            ? ['id', 'enforcement', 'check', 'prefer', 'exceptions', 'source', 'supersedes']
            : ['id', 'enforcement', 'check', 'prefer', 'exceptions', 'supersedes'],
          `${ruleSet.id} rule`,
        )
        assert(typeof rule.id === 'string' && rule.id.startsWith(`${ruleSet.id}.`), `Invalid rule id: ${rule.id}`)
        assert(!ruleIds.has(rule.id), `Duplicate rule id: ${rule.id}`)
        ruleIds.add(rule.id)
        assert(['enforce', 'advisory'].includes(rule.enforcement), `Invalid enforcement for ${rule.id}`)
        assert(typeof rule.check === 'string' && rule.check.length > 0, `${rule.id} needs a check`)
        assert(typeof rule.prefer === 'string' && rule.prefer.length > 0, `${rule.id} needs a preference`)
        if (rule.exceptions !== undefined) {
          assert(Array.isArray(rule.exceptions), `${rule.id} exceptions must be an array`)
        }
        if (policyName === 'public') {
          assert(ruleSet.sources.includes(rule.source), `${rule.id} needs an exact source from its rule set`)
        }
        if (rule.supersedes !== undefined) {
          assert(Array.isArray(rule.supersedes), `${rule.id} supersedes must be an array`)
          for (const supersededRuleId of rule.supersedes) {
            assert(!supersededRuleIds.has(supersededRuleId), `${supersededRuleId} is superseded more than once`)
            supersededRuleIds.add(supersededRuleId)
          }
        }
      }
    }
  }

  for (const supersededRuleId of supersededRuleIds) {
    assert(ruleIds.has(supersededRuleId), `Unknown superseded rule id: ${supersededRuleId}`)
  }
}

function mergeRuleSets(publicPolicy, internalPolicy) {
  return [...publicPolicy.ruleSets, ...internalPolicy.ruleSets].sort((a, b) => a.id.localeCompare(b.id))
}

function renderRule(rule, sourceLabel) {
  const lines = [
    `### \`${rule.id}\`${rule.enforcement === 'advisory' ? ' (advisory)' : ''}`,
    '',
    `- **Check:** ${rule.check}`,
    `- **Prefer:** ${rule.prefer}`,
  ]

  if (rule.exceptions?.length) {
    lines.push(`- **Exceptions:** ${rule.exceptions.join(' ')}`)
  }

  lines.push(`- **Authority:** ${sourceLabel}`, '')
  return lines.join('\n')
}

export function renderInstruction(ruleSet) {
  const digest = ruleSet.sourceDigest ?? 'sanitized-import'
  return [
    '---',
    `applyTo: '${ruleSet.applyTo}'`,
    '---',
    '',
    generatedMarker,
    '',
    `# ${ruleSet.title}`,
    '',
    `Policy set: \`${ruleSet.id}\`. Source digest: \`${digest}\`.`,
    '',
    'Review changed code against these rules. Report only concrete, actionable mismatches introduced or expanded by the change. Cite the rule ID in each finding. Do not report pre-existing migration debt unless the change makes it worse.',
    '',
    ...ruleSet.rules.map(rule =>
      renderRule(rule, rule.source ? `\`${rule.source}\`` : `Primer architecture policy: \`${rule.id}\``),
    ),
  ].join('\n')
}

function renderSkillReference(ruleSets) {
  return [
    generatedMarker,
    '',
    '# Primer component review policy',
    '',
    'Apply only the policy sets relevant to the changed files. Enforced rules may produce findings. Advisory rules should only produce a finding when the change is designing or expanding the affected contract and the mismatch has a concrete consumer impact.',
    '',
    ...ruleSets.flatMap(ruleSet => {
      return [
        `## ${ruleSet.title}`,
        '',
        `Applies to: \`${ruleSet.applyTo}\``,
        '',
        ...ruleSet.rules.map(rule =>
          renderRule(rule, rule.source ? `\`${rule.source}\`` : `Primer architecture policy: \`${rule.id}\``),
        ),
      ]
    }),
  ].join('\n')
}

function expectedFiles(ruleSets) {
  const files = new Map()
  for (const ruleSet of ruleSets) {
    files.set(join(instructionsDirectory, `${ruleSet.id}.instructions.md`), renderInstruction(ruleSet))
  }
  files.set(skillReferencePath, renderSkillReference(ruleSets))
  return files
}

function checkFiles(files) {
  const staleFiles = []
  for (const [path, contents] of files) {
    if (!existsSync(path) || readFileSync(path, 'utf8') !== `${contents}\n`) {
      staleFiles.push(relative(repositoryRoot, path))
    }
  }

  if (existsSync(instructionsDirectory)) {
    for (const filename of readdirSync(instructionsDirectory)) {
      const path = join(instructionsDirectory, filename)
      if (filename.endsWith('.instructions.md') && !files.has(path)) {
        staleFiles.push(relative(repositoryRoot, path))
      }
    }
  }

  if (staleFiles.length > 0) {
    throw new Error(
      `Generated review guidance is out of date:\n${staleFiles.map(path => `- ${path}`).join('\n')}\nRun npm run build:review-guidance.`,
    )
  }
}

function writeFiles(files) {
  mkdirSync(instructionsDirectory, {recursive: true})
  mkdirSync(dirname(skillReferencePath), {recursive: true})

  for (const filename of readdirSync(instructionsDirectory)) {
    const path = join(instructionsDirectory, filename)
    if (filename.endsWith('.instructions.md') && !files.has(path)) {
      rmSync(path)
    }
  }

  for (const [path, contents] of files) {
    writeFileSync(path, `${contents}\n`)
  }
}

function acceptSourceChanges(publicPolicy) {
  for (const ruleSet of publicPolicy.ruleSets) {
    ruleSet.sourceDigest = sourceDigest(ruleSet.sources)
  }
  writeFileSync(publicPolicyPath, `${JSON.stringify(publicPolicy, null, 2)}\n`)
}

export function build({acceptSources = false, check = false} = {}) {
  const publicPolicy = readJson(publicPolicyPath)
  const internalPolicy = readJson(internalPolicyPath)
  if (acceptSources) {
    acceptSourceChanges(publicPolicy)
  }
  validatePolicies(publicPolicy, internalPolicy)
  const files = expectedFiles(mergeRuleSets(publicPolicy, internalPolicy))

  if (check) {
    checkFiles(files)
  } else {
    writeFiles(files)
  }
}

const isMain = process.argv[1] && import.meta.url === pathToFileURL(resolve(process.argv[1])).href
if (isMain) {
  build({
    acceptSources: process.argv.includes('--accept-sources'),
    check: process.argv.includes('--check'),
  })
}
